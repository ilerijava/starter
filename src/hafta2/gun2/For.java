package hafta2.gun2;

/**
 * For sınıfı, for döngüsüne ait örnek içerir. for döngüsü önce sor sonra çalış
 * prensibine göre çalışır.
 */
public class For {

    public static void main(String[] args) {

        int i = 0;
        for (; i < 10; i++) {
            System.out.println("Merhaba Java " + i);
        }

        System.out.println(i);
    }

}
