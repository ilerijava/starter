    private void degerlendir(double sonuc) {
        if (sonuc < 18.5) {
            sonucGoster(sonuc, "ZAYIF");
        } else if (sonuc < 24.9) {
            sonucGoster(sonuc, "NORMAL");
        } else if (sonuc < 29.9) {
            sonucGoster(sonuc, "KİLOLU");
        } else {
            sonucGoster(sonuc, "AŞIRI KİLOLU");
        }
    }

    private void sonucGoster(double sonuc, String str) {
        String format = String.format("%4.2f", sonuc);
        JOptionPane.showMessageDialog(null, "sonuç : " + format + "\n" + str);
    }

