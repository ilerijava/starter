    private void hesaplaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hesaplaButtonActionPerformed
        try {
            String boyStr = boyTF.getText();
            String kiloStr = kiloTF.getText();
            if (boyStr.isEmpty() || kiloStr.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Lütfen boy ve kilo değerlerini giriniz");
                return;
            }

            boyStr = boyStr.replace(",", ".");
            kiloStr = kiloStr.replace(",", ".");

            double boyDbl = Double.parseDouble(boyStr);
            double kiloDbl = Double.parseDouble(kiloStr);

            String selectedBoy = (String) boyCB.getSelectedItem();

            if (selectedBoy.equals(Sabit.CM)) {
                boyDbl = boyDbl / 100;
            }

            if (boyDbl <= 0.5 || boyDbl >= 2.5) {
                JOptionPane.showMessageDialog(null, "Lütfen gerçekçi değer giriniz");
                return;
            }

            String selectedKilo = (String) kiloCB.getSelectedItem();

            if (selectedKilo.equals(Sabit.GR)) {
                kiloDbl = kiloDbl / 1000;
            }

            if (kiloDbl <= 10 || kiloDbl >= 300) {
                JOptionPane.showMessageDialog(null, "Lütfen gerçekçi bir değer giriniz");
                return;
            }

            double sonuc = kiloDbl / Math.pow(boyDbl, 2);

            degerlendir(sonuc);

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "Lütfen sayısal bir ifade giriniz\n"
                    + nfe.getMessage() + " hatası oluştu");
        }
    }//GEN-LAST:event_hesaplaButtonActionPerformed

